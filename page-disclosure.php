<h3>ASIC requirements</h3>
Section 47 of the National Consumer Credit Protection Act 2009 ("NCCP Act") requires that the dispute resolution system must consist of:
<ul>
 	<li>Internal dispute resolution procedure that complies with standards made or approved by ASIC which cover disputes in relation to credit activities in which we or our representatives are engaged, and</li>
 	<li>Membership of an EDR scheme approved by ASIC.</li>
</ul>
