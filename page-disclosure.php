<ul>
 	<li>Consumers have the right to be heard if they have a complaint.</li>
 	<li>We as a lender are committed to a quick and fair response to our customer complaints.</li>
 	<li>If we are unable to resolve a complaint within a reasonable period and to the satisfaction of the complainant, the matter will be referred to our External Dispute Resolution scheme â€“ AFCA.</li>
