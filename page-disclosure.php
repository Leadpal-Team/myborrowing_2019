<h3>What are Internal Dispute Resolution procedures?</h3>
Internal Dispute Resolution ('IDR') is a facility or process for a consumer to lodge a complaint with an organization. The organization is given the opportunity of resolving the complaint, failing which it could be escalated to an External
Dispute Resolution organization ('EDR') (AFCA in the case of My Borrowing). These procedures provide guidance on how to respond to a complaint and help clarify our responsibilities.</li>
 	<li>
<h3>Who can make use of these IDR Procedures?</h3>
Any borrower who interacts with My Borrowing may use the IDR process.</li>
 	<li>
<h3>What is a dispute?</h3>
The National Consumer Credit Protection Act refers to the necessity for a credit provider to have processes for dealing with "disputes". In RG 165 the words "complaint" and "dispute" are used interchangeably and for that reason for the purposes of this policy, we will take "dispute" and "complaint" as meaning the same thing.

To adopt the definition of "dispute" from The Australian Standard ISO 10002-2006, we define "dispute" as:
An expression of dissatisfaction made to us related to our products or services, or the complaints handling process itself, where a response or resolution is explicitly or implicitly expected. For the purposes of this document "dispute" also includes a "complaint", and the terms are used interchangeably, although generally a "dispute" is considered to occur when a "complaint" from a customer has not been resolved to the customer's satisfaction.</li>
